#include "libft.h"

void	ft_bzero(void *s, size_t n)
{
	size_t	cont;
	char	*s2;

	cont = 0;
	s2 = (char *) s;
	while (cont < n)
	{
		s2[cont] = '\0';
		cont++;
	}
}
